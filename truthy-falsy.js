// truthy & falsy js

// falsy
/*  false
    any negative number including 0
    empty string

*/
const x = false;
if(x){
    console.log('is truthy');
}
else{
    console.log('falsy');
}

// truthy
/*  true
    any positive number without 0
    any string including any single whitespace
*/
const y = true;
if(y){
    console.log('is truthy');
}
else{
    console.log('falsy');
}
